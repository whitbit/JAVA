CREATE TABLE `jsf_h_company`.`employee` (
  `id` INT NOT NULL COMMENT '',
  `name` VARCHAR(45) NULL COMMENT '',
  `surname` VARCHAR(45) NULL COMMENT '',
  `city` VARCHAR(45) NULL COMMENT '',
  `age` INT NULL COMMENT '',
  `birthdate` VARCHAR(45) NULL COMMENT '',
  `profession` VARCHAR(45) NULL COMMENT '',
  `branch` VARCHAR(45) NULL COMMENT '',
  `salary` VARCHAR(45) NULL COMMENT '',
  PRIMARY KEY (`id`)  COMMENT '');